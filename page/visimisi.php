	<h3>Visi & Misi</h3>
<p><br>
LSP Teknologi digital didirikan oleh Asosiasi, praktisi dan akademisi yang sudah berkecimpung lama di Sistem Sertifikasi Nasional. Pengalaman yang empiris dan harapan besar untuk mengantarkan Indonesia menjadi negara maju melalui SDM yang kompeten dan tersertifikasi, LSP TD akan terus komitmen, kontinyu dan konsisten menerapkan Sistem Manajemen Mutu pengelolaan Lembaga Sertifikasi. </p>
<p>
VISI <br>
Menjadikan Lembaga Sertifikasi Profesi bidang teknologi informasi yang profesional, kredibel, akuntabel dan terkini dalam pengembangan dan perlindungan sumberdaya manusia
</p>
MISI <br>
Menyenggarakan sertifikasi profesi/kompetensi bidang teknologi informasi yang di akui kredibilatsnya baik di dunia pendidikan maupun industri
Memiliki program kerja dan pengelolaan lembaga yang akuntabel dan dapat di pertanggungjawabkan dengan baik demi kepentingan pengembangan sumber daya manusia yang kompeten dan berakhlak mulia
Memastikan skema sertifikasi menggunakan standar kompetensi terkini dan dibutuhkan oleh dunia industri
Mengembangkan sumber daya manusia yang berkelanjutan dan berkesinambungan
Melindungi dan mengadvokasi tenaga kerja indonesia dari permasalahan hukum dan dari tenaga kerja asing yang masuk ke indonesia

